	UPDATE TQ
	SET NO_EXTRUDED_LBS = TEL.NO_LINE_RATE
	FROM #T_QA27 TQ
		INNER JOIN #T_MINUTES TEL
			ON TEL.ID_QA27 = TQ.ID_QA27


